private void mnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnmodificarActionPerformed
// TODO add your handling code here:
    int filaselecionada = tbNotas.getSelectedRow();
    if(filaselecionada>=0)
        
    {
        txtCod.setText(tbNotas.getValueAt(filaselecionada, 0).toString());
        txtNom.setText(tbNotas.getValueAt(filaselecionada, 1).toString());
        txtcre.setText(tbNotas.getValueAt(filaselecionada, 2).toString());
              
    }
    else{
        JOptionPane.showMessageDialog(null,"No ha selecionada ninguna fila a modificar" );
    }
}//GEN-LAST:event_mnmodificarActionPerformed

