private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
// TODO add your handling code here:
    
            String cod,nom,cre;
            String sql="";
            cod=txtCod.getText();
            nom=txtNom.getText();
            cre=txtcre.getText();
            sql="INSERT INTO notas (cod_cur,nom_cur,cre_cur) VALUES (?,?,?)";
        try {
              if (JOptionPane.YES_OPTION == JOptionPane.showConfirmDialog(null, "Desea Inserta a la tabla Producto :", "Insertar Datos", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)){
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, cod);
            pst.setString(2, nom);
            pst.setString(3, cre);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null, "Registro Guardado con Exito");
            cargar("");
            
            }}
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
     

}//GEN-LAST:event_btnEnviarActionPerformed

private void tbNotasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNotasMouseClicked
// TODO add your handling code here:
    int i =tbNotas.getSelectedRow();
    String cod=(String)tbNotas.getValueAt(i,0);
    String nom=(String)tbNotas.getValueAt(i,1);
    String cre=(String)tbNotas.getValueAt(i,2);
    txtCod.setText(cod);
    txtNom.setText(nom);
    txtcre.setText(cre);
    btneliminar.setEnabled(true);
}//GEN-LAST:event_tbNotasMouseClicked

